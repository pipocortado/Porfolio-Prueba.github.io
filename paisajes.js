//Modal para Paisajes.

const openModalpa = document.querySelector(".imagpa");
const modalpa = document.querySelector(".modal-pa");
const closeModalpa = document.querySelector("#pa");

openModalpa.addEventListener("click" , (e)=> {
    e.preventDefault();
    modalpa.classList.add("modal--show-p");
});

closeModalpa.addEventListener("click" , (e)=> {
    e.preventDefault();
    modalpa.classList.remove("modal--show-p");
});

